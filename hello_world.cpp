#include <iostream>

int main() {
    std::cout << "Hello, world!" << std::endl;
    std::cout << 3 + 4 << std::endl;
    std::cout << "This (\") is a quote, and this (\\) is a backslash." << std::endl; 
    std::cout << "Hello,\tworld\twith\ttabs\t!" << std::endl;
    
    std::cout << "\n#include <iostream>\n\nint main() {\n\tstd::cout << \"Hello, world!\" << std::endl;\n\n\treturn 0;\n}\n" << std::endl;

    return 0;
}
